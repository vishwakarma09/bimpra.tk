<h3 style="color:#FFF; text-align:center;">Our Story</h3>
<div style="text-align:center;" >
Nurturing the idea of empowering Indian travelers, Bimpra first came into being in 1992. What started as a humble beginning soon turned into a large organization that revolutionized the Indian travel landscape.<br /><br />

Today, our world-class travel services and innovative ideas at rock-bottom prices give us an edge to stay ahead of the curve. It is our ability to provide tailor-made holiday packages at reasonable prices that people trust us for reliability and transparency.<br /><br />

Anticipating their needs, we have continually evolved our services to cater to the modern-day holidaymaker in the global travel market. Taking heed of the ever-changing customer demands, we have carved a niche for ourselves and earned a name in providing budget friendly Southeast Asia vacation packages. In fact, we have been credited with making places like Malaysia, Hong Kong, Thailand and Singapore accessible to the common Indian with our extremely popular low-cost tour packages.<br /><br />
